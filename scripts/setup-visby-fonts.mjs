/**
 * 1) Copies font files from root Visby/ or visby/ into public/fonts/visby/ (flattened).
 * 2) Removes the root folder after a successful copy.
 * 3) Regenerates app/visby-fonts.css with @font-face rules from files in public/fonts/visby.
 *
 * Run: npm run fonts:setup
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const destDir = path.join(root, "public", "fonts", "visby");
const outCss = path.join(root, "app", "visby-fonts.css");

const FONT_EXT = new Set([".woff2", ".woff", ".otf", ".ttf"]);

function* walkFiles(dir) {
  if (!fs.existsSync(dir)) return;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) yield* walkFiles(p);
    else yield p;
  }
}

function findRootVisbyFolder() {
  for (const name of ["Visby", "visby"]) {
    const p = path.join(root, name);
    if (fs.existsSync(p) && fs.statSync(p).isDirectory()) return p;
  }
  return null;
}

function safeDestName(destDir, filePath, rootVisby) {
  const base = path.basename(filePath);
  let target = path.join(destDir, base);
  if (!fs.existsSync(target)) return target;
  const rel = path.relative(rootVisby, path.dirname(filePath));
  const prefix =
    rel && rel !== "."
      ? rel.replace(/[^\w-]+/g, "_").replace(/^_|_$/g, "")
      : "dup";
  return path.join(destDir, `${prefix}_${base}`);
}

function copyVisbyFromRoot() {
  const srcRoot = findRootVisbyFolder();
  if (!srcRoot) {
    console.log("No Visby/ or visby/ folder at repo root — skipping move.");
    return;
  }

  fs.mkdirSync(destDir, { recursive: true });

  let count = 0;
  for (const file of walkFiles(srcRoot)) {
    const ext = path.extname(file).toLowerCase();
    if (!FONT_EXT.has(ext)) continue;
    const target = safeDestName(destDir, file, srcRoot);
    fs.copyFileSync(file, target);
    count++;
  }

  if (count === 0) {
    console.warn(
      "Root Visby folder had no .woff2 / .woff / .otf / .ttf files — left folder in place.",
    );
    return;
  }

  fs.rmSync(srcRoot, { recursive: true, force: true });
  console.log(`Moved ${count} font file(s) from ${path.basename(srcRoot)}/ → public/fonts/visby/`);
}

function inferWeight(name) {
  const n = name.toLowerCase();
  if (/heavy|black|ultra/.test(n)) return 900;
  if (/extrabold|extra[-_]?bold|xbold/.test(n)) return 800;
  if (/\bbold\b|bold[-_]|[-_]bold|bd\b/.test(n)) return 700;
  if (/demi|semi/.test(n)) return 600;
  if (/medium|med\b/.test(n)) return 500;
  if (/book/.test(n)) return 450;
  if (/light|lt\b/.test(n)) return 300;
  if (/thin|hairline/.test(n)) return 100;
  if (/regular|normal|roman|standard|^visby/.test(n)) return 400;
  return 400;
}

function inferStyle(name) {
  return /italic|ital\b|oblique/i.test(name) ? "italic" : "normal";
}

function formatForExt(ext) {
  if (ext === ".woff2") return "woff2";
  if (ext === ".woff") return "woff";
  return "opentype";
}

function generateCss() {
  if (!fs.existsSync(destDir)) fs.mkdirSync(destDir, { recursive: true });

  const files = fs
    .readdirSync(destDir)
    .filter((f) => FONT_EXT.has(path.extname(f).toLowerCase()))
    .sort();

  if (files.length === 0) {
    return `/* Visby: no font files in public/fonts/visby yet.
 * Add a Visby folder at the repo root (with .woff2 / .woff / .otf) and run: npm run fonts:setup
 */\n`;
  }

  const lines = [
    "/* Generated by scripts/setup-visby-fonts.mjs — do not edit by hand */",
    "",
  ];

  for (const file of files) {
    const ext = path.extname(file).toLowerCase();
    const weight = inferWeight(file);
    const style = inferStyle(file);
    const url = `/fonts/visby/${encodeURIComponent(file)}`;
    lines.push(`@font-face {`);
    lines.push(`  font-family: "Visby CF";`);
    lines.push(`  src: url("${url}") format("${formatForExt(ext)}");`);
    lines.push(`  font-weight: ${weight};`);
    lines.push(`  font-style: ${style};`);
    lines.push(`  font-display: swap;`);
    lines.push(`}`);
    lines.push("");
  }

  return lines.join("\n");
}

copyVisbyFromRoot();
fs.writeFileSync(outCss, generateCss(), "utf8");
console.log(`Wrote ${path.relative(root, outCss)}`);
